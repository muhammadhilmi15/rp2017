			<h4>Latar Belakang Kegiatan</h4>
			<p>Resarch Party adalah perhelatan Internasional yang mengusung konsep serupa konferensi dalam disiplin ilmu Psikologi dan seluruh
				sub-bidang terkait dengan keilmuan psikologi. Tujuan Research Party adalah mempertemukan seluruh akademisi, peneliti, pendidik, dan para
				praktisi di Indonesia maupun partisipan internasional untuk bertukar fikiran, berbagi pengalaman dan hasil penelitian pada semua aspek Psikologi
				serta tema-tema terkait lainnya. Selain itu, Research Party juga menyediakan parallel session bagi para akademisi, peneliti, pendidik dan praktisi
				untuk mempresentasikan dan mendiskusikan inovasi terbaru, penemuan terkini, mengutarakan kekhawatiran serta membahas tantangan praktis
				yang dihadapi berikut solusi yang akan diterapkan di kalangan masyarakat secara lokal maupun universal.</p>
				<h4>Tema Kegiatan</h4>
				<p>Nama kegiatan ini adalah “Research Party 2017”. Sesuai dengan nama tersebut, PLC sebagai pihak penyelenggara berusaha untuk membangun
					suatu perspektif bahwa riset tidak selalu identik dengan eksistensi tekstualis semata, namun riset merupakan kegiatan yang menyenangkan, kaya
					dengan motivasi kekaryaan, juga dapat dinikmati dengan rasa ingin tahu akan penemuan dan inovasi terbaru, namun tetap membawa nuansa ilmiah
					dan semangat untuk berbagi pengetahuan baru.</p>
					<p>Mengusung tema besar “Psychology for Youth; Let’s Capture The World with Peace, Inspire, & Creativity”, yang dimunculkan sebagai
						bentuk persembahan psikologi untuk pemuda, mengkampanyekan, pentingnya pemuda yang sadar akan perdamaian, kekuatan saling menginspirasi,
						dan mengoptimalkan kreativitas untuk menangkap peluang dalam persaingan global saat ini. Melalui diskusi dan presentasi-presentasi penemuan
						terkini, diharapkan para pemuda melalui Research Party dapat memberi kontribusi secara kritis, nyata dan solutif dari dan bagi pemuda dan
						Indonesia di masa depan.</p>
						<h4>Konsep Kegiatan</h4>
						<p>Adapun konsep yang diusung dalam kegiatan ini adalah semi-
							konferensi, dengan dekorasi ruangan yang asik, mengutamakan kesan
							santai dan friendly namun tetap bertabur karya, pameran hasil riset
							disetiap sudut ruang, dan dalam tatanan yang menyenangkan untuk
							menunjukkan bahwa riset adalah sesuatu yang dapat dinikmati dengan
							caranya sendiri, melalui proses yang dilakukan dan hasil yang telah
							diperoleh para peneliti.</p>
							<p>Research Party juga dilengkapi dengan gala dinner yang dikemas
								dengan system pengelolaan acara yakni kongkow rileks dengan semangat
								mengelola/mengafirmasi waktu senggang dengan relaksasi hasil-hasil
								penelitian lintas disiplin. Diikuti oleh anak muda yang bergiat cinta idealism
								dan ketrampilan meneliti tetapi tetap bermuara pada cara perjumpaan
								anak muda.</p>
